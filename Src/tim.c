void set_fan_speed(uint8_t percent)
{
	if (percent>0)
	{
		HAL_TIM_PWM_Start(&htim14,TIM_CHANNEL_1);
		uint16_t temp_pulse;
		HAL_GPIO_WritePin(Fan_GPIO_Port, Fan_Pin, GPIO_PIN_SET);
		temp_pulse=192*percent/10;
		if (temp_pulse>1900)
			  temp_pulse=1900;
		if (temp_pulse<580)
			  temp_pulse=580;
		htim14.Instance->CCR1 = 192*percent/10;//pulse
	}
	else
	{
		HAL_GPIO_WritePin(Fan_GPIO_Port, Fan_Pin, GPIO_PIN_RESET);
		HAL_TIM_PWM_Stop(&htim14,TIM_CHANNEL_1);
	}
}

void pwm_start(void)
{
	pwm_pulse_set(StartHeatPwm);
	HAL_TIM_PWM_Start(&htim1,TIM_CHANNEL_3);	//upper igbt
	uint8_t i;
	for (i=0;i<100;i++)
	{}
  HAL_TIMEx_PWMN_Start(&htim1,TIM_CHANNEL_3);//down igbt  
}

void pwm_stop(void)
{
	HAL_TIMEx_PWMN_Stop(&htim1,TIM_CHANNEL_3);	
  HAL_TIM_PWM_Stop(&htim1,TIM_CHANNEL_3);	

}

void pwm_pulse_set(int16_t pulse)
{ 
  if (pulse>cmd.lmt_pulse)
		  pulse=cmd.lmt_pulse;
	if (pulse<StartHeatPwm)
		  pulse=StartHeatPwm;
	if (pwm_pulse>cmd.lmt_pulse)
		   pwm_pulse=cmd.lmt_pulse;
	if  (pwm_pulse<StartHeatPwm)
		   pwm_pulse=StartHeatPwm;
		
	htim1.Instance->ARR = pulse*2;//period	
	htim1.Instance->CCR3 = pulse;//pulse
	
}


/* TIM1 init function */
void MX_TIM1_Init(void)
{
  TIM_ClockConfigTypeDef sClockSourceConfig;
  TIM_MasterConfigTypeDef sMasterConfig;
  TIM_BreakDeadTimeConfigTypeDef sBreakDeadTimeConfig;
  TIM_OC_InitTypeDef sConfigOC;

  htim1.Instance = TIM1;
  htim1.Init.Prescaler = 0;
  htim1.Init.CounterMode = TIM_COUNTERMODE_UP;
  htim1.Init.Period = 800;
  htim1.Init.ClockDivision = TIM_CLOCKDIVISION_DIV1;
  htim1.Init.RepetitionCounter = 0;
  if (HAL_TIM_Base_Init(&htim1) != HAL_OK)
  {
    Error_Handler();
  }

  sClockSourceConfig.ClockSource = TIM_CLOCKSOURCE_INTERNAL;
  if (HAL_TIM_ConfigClockSource(&htim1, &sClockSourceConfig) != HAL_OK)
  {
    Error_Handler();
  }

  if (HAL_TIM_PWM_Init(&htim1) != HAL_OK)
  {
    Error_Handler();
  }

  sMasterConfig.MasterOutputTrigger = TIM_TRGO_RESET;
  sMasterConfig.MasterSlaveMode = TIM_MASTERSLAVEMODE_DISABLE;
  if (HAL_TIMEx_MasterConfigSynchronization(&htim1, &sMasterConfig) != HAL_OK)
  {
    Error_Handler();
  }

  sBreakDeadTimeConfig.OffStateRunMode = TIM_OSSR_DISABLE;
  sBreakDeadTimeConfig.OffStateIDLEMode = TIM_OSSI_DISABLE;
  sBreakDeadTimeConfig.LockLevel = TIM_LOCKLEVEL_OFF;
  sBreakDeadTimeConfig.DeadTime = 120;//180=5us,160=4us,150=3.5us,137=3us,120=2.5us
  sBreakDeadTimeConfig.BreakState = TIM_BREAK_ENABLE;
  sBreakDeadTimeConfig.BreakPolarity = TIM_BREAKPOLARITY_HIGH;
  sBreakDeadTimeConfig.AutomaticOutput = TIM_AUTOMATICOUTPUT_DISABLE;
  if (HAL_TIMEx_ConfigBreakDeadTime(&htim1, &sBreakDeadTimeConfig) != HAL_OK)
  {
    Error_Handler();
  }

  sConfigOC.OCMode = TIM_OCMODE_PWM1;
  sConfigOC.Pulse = 400;
  sConfigOC.OCPolarity = TIM_OCPOLARITY_HIGH;
  sConfigOC.OCNPolarity = TIM_OCNPOLARITY_HIGH;
  sConfigOC.OCFastMode = TIM_OCFAST_DISABLE;
  sConfigOC.OCIdleState = TIM_OCIDLESTATE_RESET;
  sConfigOC.OCNIdleState = TIM_OCNIDLESTATE_RESET;
  if (HAL_TIM_PWM_ConfigChannel(&htim1, &sConfigOC, TIM_CHANNEL_3) != HAL_OK)
  {
    Error_Handler();
  }

  HAL_TIM_MspPostInit(&htim1);
//htim1.Instance->CR1 &= ~TIM_CR1_ARPE;//Auto-reload preload disable
  htim1.Instance->CR1 = htim1.Instance->CR1|TIM_CR1_ARPE;//Auto-reload preload enable
}

